    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        btnSave.setEnabled(false);
        bd.setId(Integer.parseInt(jTable1.getValueAt(baris, 0).toString()));
        txtNamaBarang.setText(jTable1.getValueAt(baris, 1).toString());
        cbKategori.setSelectedItem(jTable1.getValueAt(baris, 2).toString());
        cbSatuan.setSelectedItem(jTable1.getValueAt(baris, 3).toString());
        txtHarga.setText(jTable1.getValueAt(baris, 4).toString());
        btnUpdate.setEnabled(true);
        
    }//GEN-LAST:event_jTable1MouseClicked

