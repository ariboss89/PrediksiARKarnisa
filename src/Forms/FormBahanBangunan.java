    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtNamaBarang.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Bahan Kosong !!");
            txtNamaBarang.requestFocus();
        }else if(cbKategori.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Pilih Kategori !!");
        }else if(cbSatuan.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Pilih Satuan !!");
        }else if(txtHarga.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harga Bahan Kosong !!");
            txtHarga.requestFocus();
        }else{
            bd.Save(txtNamaBarang.getText(), cbKategori.getSelectedItem().toString(), cbSatuan.getSelectedItem().toString(), Integer.parseInt(txtHarga.getText()));
            Refresh();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(txtNamaBarang.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Bahan Kosong !!");
            txtNamaBarang.requestFocus();
        }else if(cbKategori.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Pilih Kategori !!");
        }else if(cbSatuan.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Pilih Satuan !!");
        }else if(txtHarga.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harga Bahan Kosong !!");
            txtHarga.requestFocus();
        }else{
            bd.Update(txtNamaBarang.getText(), cbKategori.getSelectedItem().toString(), cbSatuan.getSelectedItem().toString(), Integer.parseInt(txtHarga.getText()), bd.getId());
            Refresh();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda ingin menghapus data ini ?","Konfirmasi", JOptionPane.YES_NO_OPTION);
        if(ok==0){
            bd.Delete(bd.getId());
            Refresh();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

